package com.upt.stdp;

public class Exames {

	private String nome;
	private String descr;
	public Exames(String nome, String descr) {
		super();
		this.nome = nome;
		this.descr = descr;
	}
	public String getNome() {
		return nome;
	}
	public String getDescr() {
		return descr;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setDescr(String descr) {
		this.descr = descr;
	}
	
	
}
